    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->all();

        $validate = Validator::make($data, [
            'isi_laporan' => ['required'],
            'tgl_kejadian' => ['required'],
        ]);

        if ($validate->fails()) {
            return redirect()->back()->withErrors($validate)->withInput();
        }

        if ($request->file('foto')) {
            $imageName= time().'.'.$request->file('foto')->extension();
            $request->file('foto')->move(public_path('images'), $imageName);
        }

        date_default_timezone_set('Asia/Bangkok');

        $pengaduan = Pengaduan::create([
            'tgl_pengaduan' => date('Y-m-d h:i:s'),
            'user_id' => Auth::user()->id,
            'isi_laporan' => $data['isi_laporan'],
            'foto' => 'images/'.$imageName ?? '',
            'status' => '0',
        ]);

        return redirect()->route('pengaduan.dashboard')->with(['pengaduan' => 'Berhasil terkirim!', 'type' => 'success']);
    }

